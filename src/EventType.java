
/**
 * EventType enumeration
 * Represents the type of activity event that the player can choose to perform during a day
 * Each constant map directly to an option displayed in the main game menu
 *
 * Author: [Sopheaktra Ngar]
*/
public enum EventType
{

    STUDY_ATTEND_CLASS, //Attend a Uni Class
    STUDY_ALL_NIGHTER, //All-Nighter study grind

    WORK_PART_TIME_SHIFT, //part-time shift
    WORK_OVERTIME_SHIFT, //overtime shift

    SOCIAL_HANGOUT_WITH_FRIENDS, //hangout with friends
    SOCIAL_THROW_A_PARTY, //throw a party

    SELFCARE_NETFLIX_OR_Doom_Scroll_TikTok, //netflix binge or doom scroll TikTok
    SELFCARE_GYM_OR_MEDITATION, //gym and meditation

    SKIP_DAY // skip the day
}
